package de.effnerapp.effner.data.model;

public class Term {
    private int id;
    private String type, name, date, updated_at;

    public int getId() {
        return id;
    }

    public String getType() {
        return type;
    }

    public String getName() {
        return name;
    }

    public String getDate() {
        return date;
    }

    public String getUpdateTime() {
        return updated_at;
    }
}
